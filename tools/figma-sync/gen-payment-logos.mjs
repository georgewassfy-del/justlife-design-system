/**
 * Generate packages/ui/.../PaymentMethodCard/logos.ts from the SVGs exported
 * via svg.mjs. Copies the company's licensed payment-brand marks file→file into
 * a typed data module. Skips any raster-backed SVGs (embedded base64/pattern).
 *
 * Usage: node tools/figma-sync/gen-payment-logos.mjs
 */
import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.dirname(fileURLToPath(import.meta.url));
const outDir = path.join(root, 'output');
const dest = path.resolve(root, '../../packages/ui/src/components/PaymentMethodCard/logos.ts');

// Figma node id (filename form) → logo key.
const MAP = {
  mastercard: 'svg-17632-7816.svg',
  visa: 'svg-17632-7825.svg',
  applePay: 'svg-17632-7834.svg',
  tabby: 'svg-17632-7877.svg',
  googlePay: 'svg-17632-7893.svg',
};

const lines = [
  '// AUTO-GENERATED by tools/figma-sync/gen-payment-logos.mjs — do not edit by hand.',
  '// Payment brand marks exported as SVG from the company Figma (licensed payment logos).',
  '// Rendered by PaymentLogo (img data-URI on web, react-native-svg SvgXml on native).',
  'export const paymentLogos = {',
];

for (const [name, file] of Object.entries(MAP)) {
  const p = path.join(outDir, file);
  let svg;
  try {
    svg = (await fs.readFile(p, 'utf8')).trim();
  } catch {
    console.log('missing, skip:', name);
    continue;
  }
  if (svg.includes('base64') || svg.includes('<pattern')) {
    console.log('raster-backed, skip:', name);
    continue;
  }
  const escaped = svg.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');
  lines.push(`  ${name}: \`${escaped}\`,`);
}

lines.push('} as const;', '', 'export type PaymentLogoName = keyof typeof paymentLogos;', '');
await fs.writeFile(dest, lines.join('\n'));
console.log('wrote', dest);
