/**
 * Inlines the service-category shape SVGs into the UI package as strings, so the
 * `CategoryShape` component can render them cross-platform (web <img> data-URI /
 * native SvgXml). Source of truth: apps/storybook/public/Vertical=*.svg.
 *
 * Run: node tools/figma-sync/gen-category-shapes.mjs
 */
import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '../..');
const SRC = path.join(root, 'apps/storybook/public');
const OUT = path.join(root, 'packages/ui/src/components/CategoryShape/shapes.ts');

// category key → source file
const MAP = {
  clean: 'Vertical=Clean.svg',
  heal: 'Vertical=Heal.svg',
  care: 'Vertical=Care.svg',
  assist: 'Vertical=Assist.svg',
};

const entries = [];
for (const [key, file] of Object.entries(MAP)) {
  const svg = (await fs.readFile(path.join(SRC, file), 'utf8')).trim();
  entries.push(`  ${key}: ${JSON.stringify(svg)},`);
}

const keys = Object.keys(MAP);
const ts = `// AUTO-GENERATED by tools/figma-sync/gen-category-shapes.mjs — do not edit by hand.
// Source SVGs: apps/storybook/public/Vertical=*.svg (re-run the generator to update).

/** Justlife service verticals. */
export type ServiceCategory = ${keys.map((k) => `'${k}'`).join(' | ')};

/** Raw SVG markup for each category's brand shape (40×40, colour baked in). */
export const categoryShapes: Record<ServiceCategory, string> = {
${entries.join('\n')}
};
`;

await fs.writeFile(OUT, ts);
console.log(`Wrote ${path.relative(root, OUT)} (${keys.length} shapes: ${keys.join(', ')})`);
